package com.example.googleplay;

public class Play2 {
    private int mtvImage;
    private String mtvName;
    private int mtvMore;
    private String mtvFree;

    public Play2(int mtvImage, String mtvName, int mtvMore, String mtvFree) {
        this.mtvImage = mtvImage;
        this.mtvName = mtvName;
        this.mtvMore = mtvMore;
        this.mtvFree = mtvFree;
    }

    public int getMtvImage() {
        return mtvImage;
    }

    public String getMtvName() {
        return mtvName;
    }

    public int getMtvMore() {
        return mtvMore;
    }

    public String getMtvFree() {
        return mtvFree;
    }
}
